function TestGrammarCompilation:test_apply_on_record()
	-- Given
	local buf = haka.vbuffer_from("\x42")
	local grammar = haka.grammar.new("test_apply_on_record", function ()
		test_apply_on_record = record{
			field("num", number(8)),
		}
		export(test_apply_on_record)
	end, true)

	-- When
	local result = grammar.test_apply_on_record:parse(buf:pos('begin'))

	assertEquals(result.num, 0x42)
end

function TestGrammarCompilation:test_apply_on_sequence()
	-- Given
	local buf = haka.vbuffer_from("\x42\x43")
	local grammar = haka.grammar.new("test_apply_on_sequence", function ()
		test_apply_on_sequence = sequence{
			number(8),
			record{
				field("num", number(8)),
			},
		}
		export(test_apply_on_sequence)
	end, true)

	-- When
	local result = grammar.test_apply_on_sequence:parse(buf:pos('begin'))

	assertEquals(result.num, 0x43)
end

function TestGrammarCompilation:test_apply_on_union()
	-- Given
	local buf = haka.vbuffer_from("\x42")
	local grammar = haka.grammar.new("test_apply_on_union", function ()
		test_apply_on_union = union{
			field("foo", number(4)),
			field("bar", number(8)),
		}
		export(test_apply_on_union)
	end, true)

	-- When
	local result = grammar.test_apply_on_union:parse(buf:pos('begin'))

	assertEquals(result.foo, 0x4)
	assertEquals(result.bar, 0x42)
end

