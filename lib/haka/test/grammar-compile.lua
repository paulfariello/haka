TestGrammarCompilation = {}

function TestGrammarCompilation:test_new_grammar_create_c_file()
	-- Given
	os.remove("test_new_grammar_create_c_file_grammar.c")
	local grammar = function ()
		test_new_grammar_create_c_file = record{
			field("num", number(8)),
		}:apply(function (value)
			value.ok = true
		end)

		export(test_new_grammar_create_c_file)
	end

	-- When
	haka.grammar.new("test_new_grammar_create_c_file", grammar, true)

	-- Then
	local f = assert(io.open("test_new_grammar_create_c_file_grammar.c", "r"))
	local line = f:read("*line")
	assertEquals(line, "/** File automatically generated by Haka. DO NOT EDIT */")
end

